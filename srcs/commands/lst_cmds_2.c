#include "../../includes/minishell.h"

int     ft_lstsize_cmd(t_cmds *cmd)
{
        int     i;

        i = 0;
        if (!cmd)
                return (0);
        while (cmd != NULL)
        {
                cmd = cmd->next;
                i += 1;
        }
        return (i);
}

