#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	unsigned char	*str;
	str = s;
	while (n > 0)
	{
		*(str + n - 1) = c;
		n--;
	}
	return (str);
}
